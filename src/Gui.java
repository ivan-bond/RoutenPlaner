    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Prüfen, ob Textdatei vorhanden
        try {
             sc = new Scanner(new File(jComboBox1.getSelectedItem().toString() + ".txt"));
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Gui.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        // Inhalt der Datei in String Array speichern
        for (int i=0;i<=2;i++){
            String s = sc.nextLine();
            arr[i] = s;
        }
        sc.close();
        sc.reset();
        
        // überprüfen, was für ein Material benötigt wird
        String auswahl = jComboBox2.getSelectedItem().toString();
        if("Beton".equals(auswahl)){
            x = 0;
        } else if("Holz".equals(auswahl)){
            x = 1;
        } else if("Stein".equals(auswahl)){
            x = 2;   
        }
        
        // Zeile ausgeben
        jTextArea1.append(arr[x] + "\n");
    }//GEN-LAST:event_jButton1ActionPerformed


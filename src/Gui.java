    void uhrzeit() throws IOException{
        // Systemuhrzeit bestimmen und anzeigen lassen 
        String jetzt = new SimpleDateFormat("HH:mm:ss").format(new Date());
        jLabel4.setText(jetzt);
        String now = new SimpleDateFormat("HH").format(new Date());
        int zeit = Integer.parseInt(now);
        
        // anhand der Systemzeit bestimmen, ob Tag- oder Nachtdesign eingestellt werden muss
        if (zeit >= 7 & zeit < 18){ // Tagkarte anzeigen
            ImageIcon icon = new ImageIcon("Tagkarte.png");
            jLabel3.setIcon(icon);
            getContentPane().setBackground(Color.white);
        }
        else if (zeit >= 18){ // Nachtkarte anzeigen
            jLabel3.setIcon(new ImageIcon("Nachtkarte.png")); 
            getContentPane().setBackground(Color.black);
            jLabel1.setForeground(Color.white);
            jLabel2.setForeground(Color.white);
            jLabel4.setForeground(Color.white);
            jLabel5.setForeground(Color.white);
        }
        else if (zeit < 7){ // Nachtkarte anzeigen
            jLabel3.setIcon(new ImageIcon("Nachtkarte.png"));
            getContentPane().setBackground(Color.black);
            jLabel1.setForeground(Color.white);
            jLabel2.setForeground(Color.white);
            jLabel4.setForeground(Color.white);
            jLabel5.setForeground(Color.white);
        }
    }
    
    // Systemuhrzeit jede Sekunde neu bestimmen
    public void run(){
        while(true){
            try{
                uhrzeit();
                Thread.sleep(1000);
            }
            catch(Exception e){
                e.printStackTrace();
            }
        }
    }
    
    
    
    
